<p>
Below is the description on how this plugin works and why we use this plugin. </p>
<p>This wp-vm-testimonial plugin allows you to add testimonials by the administrator and the user who visits your site. </p>
<p>And also maintains a separate list for the testimonials added by the user and wait for approval</p>
<p>once approved then it will go the list of testimonials.</p>
</p>
<p>
Once you collected some testimonials we can have the chance to move some of the testimonials to "Pool" that you like to show in the site.
if you want remove from pool you can and still that will be there in your list of testimonials.
</p>

<p>
Like this you can maintain your most liked testimonials what to display and what not to display.
</p>

<p>You can put User Testimonials form to submit their testimonials. by putting the below shot code in the post or page.</p>
<p><strong>[VM-Testimonials-Form]</strong></p>
<p>or if you want to put in your own template file use the below method.</p>
<p><strong>getVMUserTestimonialForm();</strong></p>

<p>Note: This is <strong>spam protected form with captcha</strong>....</p>

<p>
To show your selected Testimonials in the site put the below shot code in the post or page.</p>
<p><strong>[VM-Testimonials-Widget]</strong></p>
<p>or if you want to put in your own template file use the below method.</p>
<p><strong>getVMTestimonialsWeidget();</strong><p>
